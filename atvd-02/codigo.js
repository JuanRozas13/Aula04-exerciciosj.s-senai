var numero_1 = parseFloat(prompt("Digite um numero"));

var numero_2 = parseFloat(prompt("Digite mais um numero"));

if(numero_1 > numero_2){
    alert(numero_1 + " É maior que " + numero_2);
}

if(numero_1 < numero_2){
    alert(numero_1 + " É menor que " + numero_2);
}


if(numero_1 == numero_2){
    alert(numero_1 + " É exatamente igual " + numero_2);
}
